package com.qa.demo.rest.dto;

public class BandDTO
{
	private Long id;
	private String name;
	
	public BandDTO()
	{
		super();
	}

	public Long getId()
	{
		return id;
	}

	public void setId(Long id)
	{
		this.id = id;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}
	
	
}
